    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        
        int id  = Integer.parseInt(idTxt.getText());
        
        String startDate, finishDate, paid;
        
        startDate = stDateTxt.getText();
        finishDate = fDateTxt.getText();
        paid = paidTxt.getText();
        
        try {
            java.lang.Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            con = DriverManager.getConnection("jdbc:ucanaccess://C:/Database/gymSystem.accdb");
            
            pst = con.prepareStatement("update memFees set startDate=?, finishDate=?, Paid=? where id=?");
            pst.setString(1, startDate);
            pst.setString(2, finishDate);
            pst.setString(3, paid);
            pst.setInt(4, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record updated");
            
            idTxt.setText("");
            //nameTxt.setText("");
            stDateTxt.setText("");
            fDateTxt.setText("");
            paidTxt.setText("");
            //nameTxt.requestFocus();
            
            tableUpdate();
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(updateMemFees.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
            
        } catch (SQLException ex) {
            Logger.getLogger(updateMemFees.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
            
        }
        
    }//GEN-LAST:event_updateButtonActionPerformed

