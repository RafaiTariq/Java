    public void tableUpdate(){
        int c;
        try {
            java.lang.Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            con = DriverManager.getConnection("jdbc:ucanaccess://C:/Database/gymSystem.accdb");
            pst = con.prepareStatement("select * from memFees");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel dft = (DefaultTableModel)jTable1.getModel();
            dft.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for (int i=1; i<=c; i++){
                    v2.add(rs.getString("ID"));
                    v2.add(rs.getString("memName"));
                    v2.add(rs.getString("startDate"));
                    v2.add(rs.getString("finishDate"));
                    v2.add(rs.getString("Paid"));
                   
                }
                dft.addRow(v2);
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(memFees.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(memFees.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
